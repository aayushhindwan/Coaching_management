package com.example.aayushhindwan.star;

public class reco {
    String name;
    String rollno;
    String clas;
    public  reco(String name ,String rollno,String clas)
    {
        this.name=name;
        this.clas=clas;
        this.rollno=rollno;
    }

    public String getName() {
        return name;
    }

    public String getRollno() {
        return rollno;
    }

    public String getClas() {
        return clas;
    }
}
